#include "libft.h"

char	*ft_strdup(const char *s)
{
	char			*dup;
	unsigned long	len;
	unsigned long	i;

	i = 0;
	len = ft_strlen(s);
	dup = (char *)malloc(sizeof(const char) * len + 1);
	if (dup == NULL)
		return (NULL);
	while (i < len)
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
